    a0b1[12] = QCz * a0b0[ 4] + WQz * a0b0[ 5];
    a0b1[13] = QCy * a0b0[ 4] + WQy * a0b0[ 5];
    a0b1[14] = QCx * a0b0[ 4] + WQx * a0b0[ 5];
